import UIKit.UIColor

extension UIColor {
    enum Custom {
        static let background = UIColor(red: 38/255, green: 37/255, blue: 42/255, alpha: 1)
        static let placeholder = UIColor(red: 165/255, green: 171/255, blue: 175/255, alpha: 1)
        static let animateGray = UIColor(red: 51/255, green: 50/255, blue: 57/255, alpha: 1)
        static let selectedItem = UIColor.systemCyan
        static let textGray = UIColor(red: 165/255, green: 171/255, blue: 175/255, alpha: 1)
        static let textWhite = UIColor(red: 238/255, green: 238/255, blue: 241/255, alpha: 1)
    }
}
